package com.letscode.santander.coders.logica.programacao.aulas.a04;

public class While {
    public static void main(String[] args) {

        int i = 100;
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        int ii = 100;
        do  {
            System.out.println(ii);
            ii++;
        } while (ii < 10);
    }
}
